class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            NoteAppTheme {
                // A surface container using the 'background' color from the theme
                Surface(
                    modifier = Modifier.fillMaxSize(),
                    color = MaterialTheme.colorScheme.background
                ) {
                    val notesViewModel:NoteViewModel by viewModels()
                    NotesApp(notesViewModel = notesViewModel)

                }
            }
        }
    }
}

@Composable
fun NotesApp(notesViewModel: NoteViewModel){
    val noteList = notesViewModel.getAllNotes()
    NoteScreen(
        notes = noteList,
        onAddNote = {
            notesViewModel.addNote(it)
        },
        onRemoveNote = {
            notesViewModel.removeList(it)
        })
}
